package z02_eng_v2II;

class Garage {
    private boolean occupied;

    public Garage() {
        this.occupied = false;
    }

    public boolean isOccupied() {
        return occupied;
    }

    public void setOccupied(boolean occupied) {
        this.occupied = occupied;
    }

    public boolean getOccupied() {
        return this.occupied;
    }
}

